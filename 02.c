/*fromate specifiers
 >  %d  is use to print int data type value 
 >  %f is use to print float data type value
 >  %lf is use to print double data type value
 >  %c   is use to print char data type value*/ 

 #include<stdio.h>

 int main(){
    int a= 1;
    float b=2.4;
    double c=2.45;
    char d='#';
    printf( "value of a= %d\n",a);
    printf("value of b = %f\n",b);
    printf( "value of c= %lf\n",c);
    printf("value of d = %c\n",d);

 }